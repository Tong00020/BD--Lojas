    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Ordens p = new Ordens();
            OrdensDAO dao = new OrdensDAO();

            p.setId(Integer.parseInt(txtIdOrdens.getText()));
            p.setTotal(Double.parseDouble(txtPrecoTotalOrdens.getText()));
            p.setTotal_itens(Integer.parseInt(txtTotalItensOrdens.getText()));
            p.setObservation(txtObservacoesOrdens.getText());
            p.setPrice_services(Double.parseDouble(txtPrecoServicoOrdens.getText()));
            p.setSituation(txtSituacaoOrdens.getText());
            dao.create(p);

            txtIdOrdens.setText("");
            txtPrecoTotalOrdens.setText("");
            txtTotalItensOrdens.setText("");
            txtObservacoesOrdens.setText("");
            txtPrecoServicoOrdens.setText("");
            txtSituacaoOrdens.setText("");

            readJTable();

        } catch (SQLException ex) {
            Logger.getLogger(FOrdens.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTOrdens.getSelectedRow() != -1) {

            try {

                Ordens p = new Ordens();
                OrdensDAO dao = new OrdensDAO();

                p.setId((int) jTOrdens.getValueAt(jTOrdens.getSelectedRow(), 0));

                dao.delete(p);

                txtIdOrdens.setText("");
                txtPrecoTotalOrdens.setText("");
                txtTotalItensOrdens.setText("");
                txtObservacoesOrdens.setText("");
                txtPrecoServicoOrdens.setText("");
                txtSituacaoOrdens.setText("");

                readJTable();

            } catch (SQLException ex) {
                Logger.getLogger(FOrdens.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma ordem para excluir.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTOrdens.getSelectedRow() != -1) {

            try {

                Ordens p = new Ordens();
                OrdensDAO dao = new OrdensDAO();
                
                p.setId(Integer.parseInt(txtIdOrdens.getText()));
                p.setTotal(Double.parseDouble(txtPrecoTotalOrdens.getText()));
                p.setTotal_itens(Integer.parseInt(txtTotalItensOrdens.getText()));
                p.setObservation(txtObservacoesOrdens.getText());
                p.setPrice_services(Double.parseDouble(txtPrecoServicoOrdens.getText()));
                p.setSituation(txtSituacaoOrdens.getText());
                p.setId((int) jTOrdens.getValueAt(jTOrdens.getSelectedRow(), 0));

                try {
                    dao.update(p);
                } catch (SQLException ex) {
                    Logger.getLogger(FOrdens.class.getName()).log(Level.SEVERE, null, ex);
                }

                txtIdOrdens.setText("");
                txtPrecoTotalOrdens.setText("");
                txtTotalItensOrdens.setText("");
                txtObservacoesOrdens.setText("");
                txtPrecoServicoOrdens.setText("");
                txtSituacaoOrdens.setText("");
                readJTable();

            } catch (SQLException ex) {
                Logger.getLogger(FOrdens.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

