    public void readJTable() throws SQLException {
        
        DefaultTableModel modelo = (DefaultTableModel) jTOrdens.getModel();
        modelo.setNumRows(0);
        OrdensDAO pdao = new OrdensDAO();

        for (Ordens p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getTotal(),
                p.getTotal_itens(),
                p.getObservation(),
                p.getPrice_services(),
                p.getSituation()
            });

        }

    }
    
    
    public void readJTableForDesc(String desc) throws SQLException {
        
        DefaultTableModel modelo = (DefaultTableModel) jTOrdens.getModel();
        modelo.setNumRows(0);
        OrdensDAO pdao = new OrdensDAO();

        for (Ordens p : pdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getTotal(),
                p.getTotal_itens(),
                p.getObservation(),
                p.getPrice_services(),
                p.getSituation()
            });

        }
    }
    
